package com.mcnc.service.more;

public class CallBack {
	
	public static void sum(int ... args){
		int sum = 0;
		for (int i : args) {
			sum += i;
		}
		System.out.println(String.format("Result: %s", sum));
	}
	
	
	public void show(int ... args){
		System.out.println("You input:");
		for (int i : args) {
			System.out.printf("%d  \t", i);
		}
	}
	
}
